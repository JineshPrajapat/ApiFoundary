import type { FileMap, CodegenOptions } from '../../types/openapi.ts';

const NOTICE = '/* Auto-generated by ApiFoundry — do not edit */';

/**
 * Ensures path is normalized (no leading/trailing slashes)
 */
function normalizePath(path: string): string {
  return path.replace(/^\/+|\/+$/g, '');
}

/**
 * Resolves tag into:
 *  "manager/profile" → { folder: "manager", file: "profile" }
 *  "manager"         → { folder: "manager", file: "index" }
 */
function resolveTag(tag: string): { folder: string; file: string } {
  const parts = normalizePath(tag).split('/').filter(Boolean);

  const folder = parts[0];
  const file = parts[1] ?? 'index';

  return { folder, file };
}

/**
 * Builds endpoint file header.
 * Always relative to sdkDir root.
 */
function endpointHeader(depth: number): string {
  const typesImportPath = depth === 0 ? './types' : `${'../'.repeat(depth)}types`;
  return [
    NOTICE,
    `import type { RequestDescriptor } from 'apifoundry/runtime';`,
    `import type * as Types from '${typesImportPath}';`,
    '',
  ].join('\n');
}

/**
 * Converts tag to safe folder path
 * Example:
 *   "manager/profile" → ["manager", "profile"]
 */
function tagToSegments(tag: string): string[] {
  return normalizePath(tag).split('/');
}

/**
 * "user-management" → "userManagement"
 */
function camelCase(str: string): string {
  return str.replace(/-([a-z])/g, (_, c: string) => c.toUpperCase());
}

/**
 * Builds full file map with nested folder support.
 *
 * Supports:
 *   manager/profile
 *   manager/profile/settings
 *   broker/auth
 *   etc.
 */
export function buildFileMap(
  typesContent: string,
  endpointsByTag: Map<string, string>,
  options: CodegenOptions,
): FileMap {
  const { sdkDir } = options;
  const files: FileMap = {};

  // ─── Types Folder ────────────────────────────────────────────
  files[`${sdkDir}/types/index.ts`] = typesContent;

  const rootExports: string[] = [];
  
  for (const [tag, content] of endpointsByTag) {
    const segments = tagToSegments(tag); // ["manager","profile"]
    const fileName = segments.pop()!;    // "profile"
    const folderPath = segments.join('/'); // "manager"

    // Final file path
    const fullPath = folderPath
      ? `${sdkDir}/${folderPath}/${fileName}.ts`
      : `${sdkDir}/${fileName}.ts`;

    // Depth = how many levels deep from sdkDir
    // Example:
    //   api/manager/profile.ts
    //   segments = ["manager"]
    //   depth = 1
    const depth = segments.length;

    files[fullPath] = endpointHeader(depth) + content;

    // Root barrel export
    const exportPath = folderPath
      ? `./${folderPath}/${fileName}`
      : `./${fileName}`;

    rootExports.push(`export * from '${exportPath}';`);
  }

  // ─── Root Barrel ────────────────────────────────────────────
  files[`${sdkDir}/index.ts`] = [
    NOTICE,
    ...rootExports,
    `export * from './types';`,
  ].join('\n');

  return files;
}

