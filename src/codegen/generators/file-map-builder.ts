export function buildApiFileContent(
  tags: string[],
  options: CodegenOptions,
): string {
  const { sdkDir } = options;

  const imports: string[] = [];

  // Structure:
  // {
  //   manager: { auth: true, faq: true }
  //   admin: { auth: true }
  // }
  const tree: Record<string, Record<string, string>> = {};

  for (const tag of tags) {
    const segments = tagToSegments(tag); // ["manager","auth"]

    const root = segments[0];            // "manager"
    const child = segments[1] ?? 'index';

    if (!tree[root]) tree[root] = {};

    tree[root][child] = tag; // keep original tag for path resolution
  }

  // Generate imports
  for (const root in tree) {
    for (const child in tree[root]) {
      const importVar = `${root}_${child}`; // unique variable name

      const importPath =
        child === 'index'
          ? `./${sdkDir}/${root}`
          : `./${sdkDir}/${root}/${child}`;

      imports.push(`import * as ${importVar} from '${importPath}';`);
    }
  }

  // Build nested object string
  const nestedObject: string[] = [];

  for (const root in tree) {
    nestedObject.push(`    ${root}: {`);

    for (const child in tree[root]) {
      const importVar = `${root}_${child}`;
      nestedObject.push(`      ...${importVar},`);
    }

    nestedObject.push(`    },`);
  }

  return `/**
 * API Client
 *
 * Generated once by ApiFoundry — this file is yours to configure.
 * Re-running "apifoundry generate" will NOT overwrite it.
 */
import { createClient } from 'apifoundry/runtime';
${imports.join('\n')}

// ─── Option A: Built-in fetch ───────────────────────────────────
export const api = createClient(
  {
${nestedObject.join('\n')}
  },
  {
    baseUrl: process.env.NEXT_PUBLIC_API_URL ?? process.env.VITE_API_URL ?? '',
  },
);

// ─── Option B: Axios ────────────────────────────────────────────
// import { axiosInstance } from './lib/axios';
// export const api = createClient({ ... }, { axiosInstance });

// ─── Option C: Custom Adapter ───────────────────────────────────
// import type { HttpAdapter } from 'apifoundry/runtime';
// const adapter: HttpAdapter = { execute: async (d) => { /* ... */ } };
// export const api = createClient({ ... }, { adapter });

export type Api = typeof api;
`;
}